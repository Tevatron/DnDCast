let editorSessions  = [];
let editorCampaigns = [];
let isDirty         = false;

let editingSceneIdx    = null; // null = new item
let editingSessionIdx  = null;
let editingCampaignIdx = null;
let editingSessionScenes = []; // working copy of session.scenes during edit
let idAutoGenerated = true;    // tracks whether scene/session ID was auto-slugified

