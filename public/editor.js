// === Init =============================================================

function init() {
  // Tab switching
  tabBtns.forEach(btn => btn.addEventListener('click', () => switchTab(btn.dataset.tab)));

  // Header actions
  saveBtn.addEventListener('click', saveProject);
  loadFromServer();

  // Scene tab
  addSceneBtn.addEventListener('click', () => openSceneEdit(null));
  sceneForm.addEventListener('submit', e => { e.preventDefault(); saveScene(); });
  deleteSceneBtn.addEventListener('click', deleteScene);
  sceneForm.querySelector('[name="silent"]').addEventListener('change', syncSilentState);

  // Auto-slugify scene title → id
  const sceneTitle = sceneForm.querySelector('[name="title"]');
  const sceneId    = sceneForm.querySelector('[name="id"]');
  sceneTitle.addEventListener('input', () => {
    if (idAutoGenerated) sceneId.value = slugify(sceneTitle.value);
  });
  sceneId.addEventListener('input', () => { idAutoGenerated = false; });

  // Adventure tab
  addAdventureBtn.addEventListener('click', () => openAdventureEdit(null));
  adventureForm.addEventListener('submit', e => { e.preventDefault(); saveAdventure(); });
  deleteAdventureBtn.addEventListener('click', deleteAdventure);
  scenePickerSearch.addEventListener('input', filterScenePicker);
  // Changing the campaign changes which campaign-private scenes are offered.
  adventureCampaignSelect.addEventListener('change', () => {
    if (!adventureEditPanel.hidden) renderScenePicker();
  });

  // Auto-slugify adventure title → id
  const adventureTitle = adventureForm.querySelector('[name="title"]');
  const adventureId    = adventureForm.querySelector('[name="id"]');
  adventureTitle.addEventListener('input', () => {
    if (idAutoGenerated) adventureId.value = slugify(adventureTitle.value);
  });
  adventureId.addEventListener('input', () => { idAutoGenerated = false; });

  // Campaign tab
  addCampaignBtn.addEventListener('click', () => openCampaignEdit(null));
  campaignForm.addEventListener('submit', e => { e.preventDefault(); saveCampaign(); });
  deleteCampaignBtn.addEventListener('click', deleteCampaign);

  // Auto-slugify campaign title → id
  const campaignTitle = campaignForm.querySelector('[name="title"]');
  const campaignId    = campaignForm.querySelector('[name="id"]');
  campaignTitle.addEventListener('input', () => {
    if (idAutoGenerated) campaignId.value = slugify(campaignTitle.value);
  });
  campaignId.addEventListener('input', () => { idAutoGenerated = false; });

  // Close-edit buttons (close panel without saving)
  document.querySelectorAll('.close-edit-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const panelId = btn.dataset.panel;
      if (panelId) $(panelId).hidden = true;
    });
  });

  // Browse buttons (FSA, Chrome/Edge only — helps fill in asset paths)
  const canBrowse = 'showOpenFilePicker' in window;
  document.querySelectorAll('.browse-btn').forEach(btn => {
    if (!canBrowse) { btn.classList.add('hidden'); return; }
    btn.addEventListener('click', () => browseFile(btn.closest('form'), btn.dataset.field, btn.dataset.type));
  });

  // Warn before leaving with unsaved changes
  window.addEventListener('beforeunload', e => {
    if (isDirty) { e.preventDefault(); e.returnValue = ''; }
  });
}

// === Data I/O (server API) ============================================

async function loadFromServer() {
  try {
    const res = await fetch('/api/data');
    if (res.status === 401) { location.href = '/login'; return; }
    if (!res.ok) throw new Error('HTTP ' + res.status);
    const data       = await res.json();
    editorScenes     = Array.isArray(data.scenes)     ? data.scenes     : [];
    editorAdventures = Array.isArray(data.adventures) ? data.adventures : [];
    editorCampaigns  = Array.isArray(data.campaigns)  ? data.campaigns  : [];
    isDirty = false;
    renderAll();
  } catch (e) {
    showStatus('Could not load data: ' + e.message, 'error');
  }
}

async function saveProject() { await saveToServer(); }
async function autoSave()    { await saveToServer(); }

async function saveToServer() {
  try {
    const res = await fetch('/api/save', {
      method:  'POST',
      headers: { 'Content-Type': 'application/json' },
      body:    JSON.stringify({
        scenes:     editorScenes,
        adventures: editorAdventures,
        campaigns:  editorCampaigns,
      }),
    });
    if (res.status === 401) { location.href = '/login'; return; }
    if (!res.ok) throw new Error('HTTP ' + res.status);
    flashSaveBtn('Saved ✓');
    isDirty = false;
  } catch (e) {
    showStatus('Save failed: ' + e.message, 'error');
  }
}

function flashSaveBtn(text) {
  const orig = saveBtn.textContent;
  saveBtn.textContent = text;
  setTimeout(() => { saveBtn.textContent = orig; }, 2000);
}

async function browseFile(form, fieldName, fileType) {
  const isImage = fileType === 'image';
  const types   = isImage
    ? [{ description: 'Images', accept: { 'image/*': ['.jpg', '.jpeg', '.png', '.webp', '.gif'] } }]
    : [{ description: 'Audio',  accept: { 'audio/*': ['.mp3', '.ogg', '.wav', '.m4a', '.flac'] } }];
  try {
    const [fh]  = await window.showOpenFilePicker({ types });
    const file  = await fh.getFile();

    showStatus(`Uploading ${file.name}…`, 'info');
    const body = new FormData();
    body.append('file', file);
    const res = await fetch('/api/upload', { method: 'POST', body });
    if (!res.ok) throw new Error('Upload failed: HTTP ' + res.status);
    const { path } = await res.json();

    const input = (form || sceneForm).querySelector('[name="' + fieldName + '"]');
    if (input) input.value = path;
    showStatus(`Uploaded: ${file.name}`, 'success');
  } catch (e) {
    if (e.name !== 'AbortError') showStatus('File error: ' + e.message, 'error');
  }
}

// === Tab Management ===================================================

function switchTab(tabName) {
  tabBtns.forEach(b  => b.classList.toggle('active', b.dataset.tab === tabName));
  tabPanels.forEach(p => p.hidden = p.id !== 'tab-' + tabName);
}

// === Render All =======================================================

function renderAll() {
  renderScenes();
  renderAdventures();
  renderCampaigns();
  populateCampaignDropdown();
  populateSceneOwnerDropdown();
}

// Owner choices for a scene's "Private to" field: every campaign and adventure.
function populateSceneOwnerDropdown() {
  if (!scenePrivateSelect) return;
  const current = scenePrivateSelect.value;
  scenePrivateSelect.innerHTML = '<option value="">Public — available everywhere</option>';
  const addGroup = (label, items) => {
    if (!items.length) return;
    const og = document.createElement('optgroup');
    og.label = label;
    items.forEach(it => {
      const o = document.createElement('option');
      o.value = it.id;
      o.textContent = it.title || it.id;
      og.appendChild(o);
    });
    scenePrivateSelect.appendChild(og);
  };
  addGroup('Campaigns', editorCampaigns);
  addGroup('Adventures', editorAdventures);
  scenePrivateSelect.value = current;
}

// === Scenes ===========================================================

function renderScenes() {
  scenesList.innerHTML = '';
  scenesEmpty.hidden   = editorScenes.length > 0;

  editorScenes.forEach((scene, i) => {
    const li = document.createElement('li');
    li.className = 'list-item';
    li.innerHTML =
      `<div class="list-item-body">` +
      `<div class="list-item-title">${escHtml(scene.title || scene.id || 'Untitled')}</div>` +
      `<div class="list-item-meta">${escHtml(scene.id)}` +
      (scene.image ? '' : ' · <em>no image</em>') +
      (scene.audio ? '' : ' · <em>no audio</em>') +
      (scene.privateTo ? ' · <em>private</em>' : '') +
      `</div></div>`;
    li.addEventListener('click', () => openSceneEdit(i));
    scenesList.appendChild(li);
  });
}

function openSceneEdit(idx) {
  editingSceneIdx = idx;
  idAutoGenerated = idx === null; // auto-slug only for new scenes
  sceneEditHeading.textContent = idx === null ? 'New Scene' : 'Edit Scene';
  deleteSceneBtn.style.display = idx === null ? 'none' : '';

  // Highlight active item
  Array.from(scenesList.children).forEach((li, i) => li.classList.toggle('active', i === idx));

  const scene = idx !== null ? editorScenes[idx] : {};
  const f = sceneForm;
  f.querySelector('[name="title"]').value    = scene.title    || '';
  f.querySelector('[name="id"]').value       = scene.id       || '';
  f.querySelector('[name="image"]').value    = scene.image    || '';
  f.querySelector('[name="audio"]').value    = scene.audio    || '';
  f.querySelector('[name="notes"]').value    = scene.notes    || '';
  f.querySelector('[name="dmScript"]').value = scene.dmScript || '';
  f.querySelector('[name="fit"]').value      = scene.fit === 'cover' ? 'cover' : 'contain';
  f.querySelector('[name="loopAudio"]').checked = scene.loopAudio !== false;
  f.querySelector('[name="silent"]').checked    = !!scene.silent;
  syncSilentState();
  populateSceneOwnerDropdown();
  f.querySelector('[name="privateTo"]').value   = scene.privateTo || '';

  sceneEditPanel.hidden = false;
}

// "No music" makes the custom audio path irrelevant — grey it out when checked.
function syncSilentState() {
  const silent = sceneForm.querySelector('[name="silent"]').checked;
  sceneForm.querySelector('[name="audio"]').disabled = silent;
  const browse = sceneForm.querySelector('.browse-btn[data-field="audio"]');
  if (browse) browse.disabled = silent;
}

function saveScene() {
  const f = sceneForm;
  const id    = f.querySelector('[name="id"]').value.trim();
  const title = f.querySelector('[name="title"]').value.trim();

  if (!id) { showStatus('ID is required.', 'error'); return; }

  // Check for duplicate ID (excluding the item being edited)
  const duplicate = editorScenes.some((s, i) => s.id === id && i !== editingSceneIdx);
  if (duplicate) { showStatus('A scene with this ID already exists.', 'error'); return; }

  const scene = {
    id,
    title:     title                                          || '',
    image:     f.querySelector('[name="image"]').value.trim()|| '',
    audio:     f.querySelector('[name="audio"]').value.trim()|| '',
    notes:     f.querySelector('[name="notes"]').value.trim()|| '',
    dmScript:  f.querySelector('[name="dmScript"]').value.trim() || '',
    fit:       f.querySelector('[name="fit"]').value,
    loopAudio: f.querySelector('[name="loopAudio"]').checked,
    silent:    f.querySelector('[name="silent"]').checked,
    privateTo: f.querySelector('[name="privateTo"]').value || '',
  };

  // Remove fields that match defaults to keep JSON clean
  if (!scene.image)            delete scene.image;
  if (!scene.audio)            delete scene.audio;
  if (!scene.notes)            delete scene.notes;
  if (!scene.dmScript)         delete scene.dmScript;
  if (!scene.silent)           delete scene.silent;
  if (!scene.privateTo)        delete scene.privateTo;
  if (scene.fit === 'contain') delete scene.fit;  // contain is the default; omit it

  if (editingSceneIdx !== null) {
    const oldId = editorScenes[editingSceneIdx].id;
    editorScenes[editingSceneIdx] = scene;
    if (oldId !== id) showStatus('Scene ID changed — check adventure references.', 'info');
  } else {
    editorScenes.push(scene);
  }

  isDirty = true;
  sceneEditPanel.hidden = true;
  editingSceneIdx = null;
  renderScenes();
  autoSave();
}

function deleteScene() {
  if (editingSceneIdx === null) return;
  const scene = editorScenes[editingSceneIdx];
  const refs  = editorAdventures.filter(a => a.scenes && a.scenes.includes(scene.id));
  const warn  = refs.length ? ` ${refs.length} adventure(s) reference this scene and will show a broken-ref warning.` : '';
  if (!confirm('Delete "' + (scene.title || scene.id) + '"?' + warn)) return;
  editorScenes.splice(editingSceneIdx, 1);
  isDirty = true;
  sceneEditPanel.hidden = true;
  editingSceneIdx = null;
  renderScenes();
  renderAdventures();
  autoSave();
}

// === Adventures =======================================================

function renderAdventures() {
  adventuresList.innerHTML = '';
  adventuresEmpty.hidden   = editorAdventures.length > 0;

  editorAdventures.forEach((adventure, i) => {
    const campaign  = editorCampaigns.find(c => c.id === adventure.campaignId);
    const sceneCount = Array.isArray(adventure.scenes) ? adventure.scenes.length : 0;
    const broken    = (adventure.scenes || []).filter(id => !editorScenes.find(s => s.id === id));

    const li = document.createElement('li');
    li.className = 'list-item';
    li.innerHTML =
      `<div class="list-item-body">` +
      `<div class="list-item-title">${escHtml(adventure.title || adventure.id || 'Untitled')}</div>` +
      `<div class="list-item-meta">` +
      (campaign ? escHtml(campaign.title) + ' · ' : '') +
      `${sceneCount} scene${sceneCount !== 1 ? 's' : ''}</div></div>` +
      (broken.length ? `<span class="warn-badge">⚠ ${broken.length} missing</span>` : '');
    li.addEventListener('click', () => openAdventureEdit(i));
    adventuresList.appendChild(li);
  });
}

function openAdventureEdit(idx) {
  editingAdventureIdx = idx;
  idAutoGenerated     = idx === null;
  adventureEditHeading.textContent = idx === null ? 'New Adventure' : 'Edit Adventure';
  deleteAdventureBtn.style.display = idx === null ? 'none' : '';

  Array.from(adventuresList.children).forEach((li, i) => li.classList.toggle('active', i === idx));

  const adventure = idx !== null ? editorAdventures[idx] : {};
  const f = adventureForm;
  f.querySelector('[name="title"]').value      = adventure.title      || '';
  f.querySelector('[name="id"]').value         = adventure.id         || '';
  f.querySelector('[name="soundtrack"]').value = adventure.soundtrack || '';

  populateCampaignDropdown();
  adventureCampaignSelect.value = adventure.campaignId || '';

  editingAdventureScenes = Array.isArray(adventure.scenes) ? [...adventure.scenes] : [];
  renderScenePicker();
  renderAdventureSceneList();

  adventureEditPanel.hidden = false;
}

function saveAdventure() {
  const f  = adventureForm;
  const id    = f.querySelector('[name="id"]').value.trim();
  const title = f.querySelector('[name="title"]').value.trim();
  if (!id) { showStatus('ID is required.', 'error'); return; }

  const duplicate = editorAdventures.some((a, i) => a.id === id && i !== editingAdventureIdx);
  if (duplicate) { showStatus('An adventure with this ID already exists.', 'error'); return; }

  const adventure = {
    id,
    title:      title || '',
    campaignId: adventureCampaignSelect.value || undefined,
    soundtrack: f.querySelector('[name="soundtrack"]').value.trim() || '',
    scenes:     [...editingAdventureScenes],
  };
  if (!adventure.campaignId) delete adventure.campaignId;
  if (!adventure.soundtrack) delete adventure.soundtrack;

  if (editingAdventureIdx !== null) {
    editorAdventures[editingAdventureIdx] = adventure;
  } else {
    editorAdventures.push(adventure);
  }

  isDirty = true;
  adventureEditPanel.hidden = true;
  editingAdventureIdx = null;
  renderAdventures();
  autoSave();
}

function deleteAdventure() {
  if (editingAdventureIdx === null) return;
  const a = editorAdventures[editingAdventureIdx];
  if (!confirm('Delete adventure "' + (a.title || a.id) + '"?')) return;
  editorAdventures.splice(editingAdventureIdx, 1);
  isDirty = true;
  adventureEditPanel.hidden = true;
  editingAdventureIdx = null;
  renderAdventures();
  autoSave();
}

function renderAdventureSceneList() {
  adventureSceneList.innerHTML = '';
  adventureSceneEmpty.hidden   = editingAdventureScenes.length > 0;

  editingAdventureScenes.forEach((sceneId, i) => {
    const scene = editorScenes.find(s => s.id === sceneId);
    const li    = document.createElement('li');
    li.className = 'adventure-scene-row';
    li.draggable = true;
    wireSceneDrag(li, i);

    const handle = document.createElement('span');
    handle.className   = 'scene-drag-handle';
    handle.textContent = '⠿';
    handle.title       = 'Drag to reorder';

    const titleEl = document.createElement('span');
    titleEl.className   = 'adventure-scene-title';
    titleEl.textContent = scene ? (scene.title || sceneId) : '⚠ ' + sceneId + ' (missing)';
    if (!scene) titleEl.style.color = '#804040';

    const idEl = document.createElement('span');
    idEl.className   = 'adventure-scene-id';
    idEl.textContent = sceneId;

    const upBtn = document.createElement('button');
    upBtn.className   = 'scene-order-btn';
    upBtn.title       = 'Move up';
    upBtn.textContent = '↑';
    upBtn.disabled    = i === 0;
    upBtn.addEventListener('click', () => moveScene(i, -1));

    const downBtn = document.createElement('button');
    downBtn.className   = 'scene-order-btn';
    downBtn.title       = 'Move down';
    downBtn.textContent = '↓';
    downBtn.disabled    = i === editingAdventureScenes.length - 1;
    downBtn.addEventListener('click', () => moveScene(i, 1));

    const removeBtn = document.createElement('button');
    removeBtn.className   = 'scene-remove-btn';
    removeBtn.title       = 'Remove from adventure';
    removeBtn.textContent = '✕';
    removeBtn.addEventListener('click', () => removeSceneFromAdventure(i));

    li.append(handle, titleEl, idEl, upBtn, downBtn, removeBtn);
    adventureSceneList.appendChild(li);
  });

  updatePickerCheckboxes();
}

// HTML5 drag-and-drop reordering for a selected-scene row at index i.
function wireSceneDrag(li, i) {
  li.addEventListener('dragstart', e => {
    dragFromIdx = i;
    li.classList.add('dragging');
    e.dataTransfer.effectAllowed = 'move';
  });
  li.addEventListener('dragend', () => {
    dragFromIdx = null;
    adventureSceneList.querySelectorAll('.adventure-scene-row')
      .forEach(r => r.classList.remove('dragging', 'drag-over'));
  });
  li.addEventListener('dragover', e => {
    e.preventDefault();                       // allow drop
    e.dataTransfer.dropEffect = 'move';
    if (dragFromIdx !== null && dragFromIdx !== i) li.classList.add('drag-over');
  });
  li.addEventListener('dragleave', () => li.classList.remove('drag-over'));
  li.addEventListener('drop', e => {
    e.preventDefault();
    if (dragFromIdx === null || dragFromIdx === i) return;
    const [moved] = editingAdventureScenes.splice(dragFromIdx, 1);
    editingAdventureScenes.splice(i, 0, moved);
    renderAdventureSceneList();
  });
}

function filterScenePicker() { filterList(scenePickerSearch, scenePicker, '.picker-row'); }

// Build the full checkbox picker — called once when the adventure editor opens.
// Preserves scroll position on reorder by only syncing checkbox state after that.
function renderScenePicker() {
  scenePickerSearch.value = '';
  scenePicker.innerHTML = '';
  if (!editorScenes.length) {
    const empty = document.createElement('p');
    empty.className   = 'empty-state small';
    empty.textContent = 'No scenes exist yet. Create scenes first.';
    scenePicker.appendChild(empty);
    return;
  }
  editorScenes.forEach(scene => {
    if (!sceneAllowedInAdventure(scene)) return;   // hide other owners' private scenes

    const row = document.createElement('label');
    row.className = 'picker-row';

    const cb = document.createElement('input');
    cb.type  = 'checkbox';
    cb.value = scene.id;
    cb.checked = editingAdventureScenes.includes(scene.id);
    cb.addEventListener('change', () => {
      if (cb.checked) {
        if (!editingAdventureScenes.includes(scene.id)) editingAdventureScenes.push(scene.id);
      } else {
        const i = editingAdventureScenes.indexOf(scene.id);
        if (i !== -1) editingAdventureScenes.splice(i, 1);
      }
      renderAdventureSceneList();
    });

    const label = document.createElement('span');
    label.textContent = (scene.title || scene.id) + (scene.privateTo ? ' · private' : '');

    row.append(cb, label);
    scenePicker.appendChild(row);
  });
}

// A scene is offered for the adventure being edited when it's public, owned by
// this adventure (or its campaign), or already in the adventure (so it can be
// removed). Uses the live form values so it tracks edits before saving.
function sceneAllowedInAdventure(scene) {
  if (!scene.privateTo) return true;
  if (editingAdventureScenes.includes(scene.id)) return true;
  const advId  = adventureForm.querySelector('[name="id"]').value.trim();
  const campId = adventureCampaignSelect.value;
  return scene.privateTo === advId || (!!campId && scene.privateTo === campId);
}

// Sync checked state without rebuilding — keeps scroll position intact.
function updatePickerCheckboxes() {
  scenePicker.querySelectorAll('input[type="checkbox"]').forEach(cb => {
    cb.checked = editingAdventureScenes.includes(cb.value);
  });
}

function removeSceneFromAdventure(idx) {
  editingAdventureScenes.splice(idx, 1);
  renderAdventureSceneList();
}

function moveScene(idx, dir) {
  const dest = idx + dir;
  if (dest < 0 || dest >= editingAdventureScenes.length) return;
  [editingAdventureScenes[idx], editingAdventureScenes[dest]] = [editingAdventureScenes[dest], editingAdventureScenes[idx]];
  renderAdventureSceneList();
}

// === Campaigns ========================================================

function renderCampaigns() {
  campaignsList.innerHTML = '';
  campaignsEmpty.hidden   = editorCampaigns.length > 0;

  editorCampaigns.forEach((campaign, i) => {
    const adventureCount = editorAdventures.filter(a => a.campaignId === campaign.id).length;
    const li = document.createElement('li');
    li.className = 'list-item';
    li.innerHTML =
      `<div class="list-item-body">` +
      `<div class="list-item-title">${escHtml(campaign.title || campaign.id || 'Untitled')}</div>` +
      `<div class="list-item-meta">${adventureCount} adventure${adventureCount !== 1 ? 's' : ''}` +
      (campaign.description ? ' · ' + escHtml(campaign.description) : '') +
      `</div></div>`;
    li.addEventListener('click', () => openCampaignEdit(i));
    campaignsList.appendChild(li);
  });

  populateCampaignDropdown();
}

function populateCampaignDropdown() {
  const current = adventureCampaignSelect ? adventureCampaignSelect.value : '';
  if (!adventureCampaignSelect) return;
  adventureCampaignSelect.innerHTML = '<option value="">(none)</option>';
  editorCampaigns.forEach(c => {
    const opt = document.createElement('option');
    opt.value       = c.id;
    opt.textContent = c.title || c.id;
    adventureCampaignSelect.appendChild(opt);
  });
  adventureCampaignSelect.value = current;
}

function openCampaignEdit(idx) {
  editingCampaignIdx = idx;
  idAutoGenerated    = idx === null;
  campaignEditHeading.textContent = idx === null ? 'New Campaign' : 'Edit Campaign';
  deleteCampaignBtn.style.display = idx === null ? 'none' : '';

  Array.from(campaignsList.children).forEach((li, i) => li.classList.toggle('active', i === idx));

  const campaign = idx !== null ? editorCampaigns[idx] : {};
  const f = campaignForm;
  f.querySelector('[name="title"]').value       = campaign.title       || '';
  f.querySelector('[name="id"]').value          = campaign.id          || '';
  f.querySelector('[name="description"]').value = campaign.description || '';

  campaignEditPanel.hidden = false;
}

function saveCampaign() {
  const f  = campaignForm;
  const id    = f.querySelector('[name="id"]').value.trim();
  const title = f.querySelector('[name="title"]').value.trim();
  if (!id) { showStatus('ID is required.', 'error'); return; }

  const duplicate = editorCampaigns.some((c, i) => c.id === id && i !== editingCampaignIdx);
  if (duplicate) { showStatus('A campaign with this ID already exists.', 'error'); return; }

  const campaign = {
    id,
    title:       title || '',
    description: f.querySelector('[name="description"]').value.trim() || '',
  };
  if (!campaign.description) delete campaign.description;

  if (editingCampaignIdx !== null) {
    editorCampaigns[editingCampaignIdx] = campaign;
  } else {
    editorCampaigns.push(campaign);
  }

  isDirty = true;
  campaignEditPanel.hidden = true;
  editingCampaignIdx = null;
  renderCampaigns();
  autoSave();
}

function deleteCampaign() {
  if (editingCampaignIdx === null) return;
  const c    = editorCampaigns[editingCampaignIdx];
  const refs = editorAdventures.filter(a => a.campaignId === c.id);
  const warn = refs.length ? ` ${refs.length} adventure(s) belong to this campaign and will lose their campaign link.` : '';
  if (!confirm('Delete campaign "' + (c.title || c.id) + '"?' + warn)) return;
  editorCampaigns.splice(editingCampaignIdx, 1);
  isDirty = true;
  campaignEditPanel.hidden = true;
  editingCampaignIdx = null;
  renderCampaigns();
  renderAdventures();
  autoSave();
}

// === Helpers ==========================================================

function slugify(text) {
  return text.toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/[\s]+/g, '-')
    .replace(/-+/g, '-');
}

function escHtml(str) {
  return String(str)
    .replace(/&/g, '&amp;').replace(/</g, '&lt;')
    .replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}

let statusTimer = null;
function showStatus(msg, type) {
  statusBar.textContent = msg;
  statusBar.className   = type;
  statusBar.hidden      = false;
  clearTimeout(statusTimer);
  statusTimer = setTimeout(() => { statusBar.hidden = true; }, 4000);
}

