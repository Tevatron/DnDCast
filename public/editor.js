let editingAdventureIdx  = null;
let editingCampaignIdx   = null;
let editingAdventureScenes = []; // working copy of adventure.scenes during edit
let editingSceneImages   = [];   // working copy of a scene's image list during edit
let idAutoGenerated = true;      // tracks whether ID was auto-slugified
let dragFromIdx = null;          // source index during scene drag-reorder
